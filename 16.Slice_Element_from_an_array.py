arr = [1, 2, 3, 4, 5]
start_index = 1
end_index = 4
result = arr[start_index:end_index]
print(result)
#arr = [1, 2, 3, 4, 5]: Tạo một mảng có tên là arr với các phần tử là 1, 2, 3, 4, 5.
#start_index = 1: Khởi tạo biến start_index với giá trị là 1. Đây là vị trí bắt đầu của phần cắt.
#end_index = 4: Khởi tạo biến end_index với giá trị là 4. Đây là vị trí kết thúc của phần cắt.
#result = arr[start_index:end_index]: Sử dụng cú pháp slicing để cắt mảng arr từ vị trí start_index đến vị trí end_index.
#Kết quả của phép cắt được gán cho biến result.
#print(result): In ra màn hình giá trị của biến result.
#Khi chạy đoạn code này, kết quả sẽ là một mảng mới chứa các phần tử từ vị trí có index 1 đến vị trí có index 3 
#In kết quả (không bao gồm phần tử có index kết thúc).