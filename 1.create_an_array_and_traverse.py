my_array = [1, 2, 3, 4, 5]

print("Array elements:")
for element in my_array:
    print(element)
#my_array = [1, 2, 3, 4, 5]: Tạo một mảng (list) có tên là my_array chứa các số từ 1 đến 5.
#print("Array elements:"): In ra màn hình một dòng chữ "Array elements:", nhằm làm tiêu đề cho các phần tử của mảng.
#for element in my_array:: Bắt đầu vòng lặp for để duyệt qua từng phần tử trong mảng my_array.
#print(element): Trong mỗi vòng lặp, in ra màn hình giá trị của phần tử hiện tại trong mảng.
#Vòng lặp này sẽ chạy qua từng phần tử trong mảng và in chúng lần lượt.