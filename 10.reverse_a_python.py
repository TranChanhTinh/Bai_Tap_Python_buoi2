array=[1,2,3,4,5]#Dòng này tạo một danh sách (list) có tên là array chứa các giá trị [1, 2, 3, 4, 5]

print('mang chua dao:', array)#Dòng này in ra màn hình danh sách (array) trước khi đảo ngược.

array.reverse()#Dòng này sử dụng phương thức reverse() để đảo ngược thứ tự của các phần tử trong danh sách array. 
#Sau khi thực hiện dòng này, danh sách sẽ được đảo ngược

print('mang da dao', array)#Dòng này in ra màn hình danh sách (array) sau khi đã đảo ngược. 