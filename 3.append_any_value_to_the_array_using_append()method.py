my_array = [1, 2, 3, 4, 5]

print("Original Array:", my_array)

new_value = 6
my_array.append(new_value)

print("Array after appending", new_value, ":", my_array)
#print("Original Array:", my_array): Dòng này in ra màn hình mảng gốc để kiểm tra giá trị ban đầu của mảng.
#new_value = 6: Dòng này tạo một biến new_value và gán giá trị là 6.
#my_array.append(new_value): Dòng này sử dụng phương thức append() để thêm giá trị mới (new_value) vào cuối mảng my_array.
